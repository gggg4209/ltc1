package com.example.ltc20;

import android.content.Context;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ImageView;

import androidx.viewpager.widget.PagerAdapter;

public class ImageAdapterltc4_3_1 extends PagerAdapter {

    private Context mContext;
    private int[] mImageIds = new int[]{R.drawable.ltc4310 ,R.drawable.ltc4311 ,R.drawable.ltc4312 ,R.drawable.ltc4313 ,R.drawable.ltc4314 ,R.drawable.ltc43105  };
    ImageAdapterltc4_3_1(Context context) { mContext = context; }
    @Override
    public int getCount() { return mImageIds.length; }
    @Override
    public boolean isViewFromObject(View view, Object object) {
        return view == object;
    }
    @Override
    public Object instantiateItem(ViewGroup container, int position) {
        ImageView imageView = new ImageView(mContext);
        imageView.setScaleType(ImageView.ScaleType.CENTER_CROP);
        imageView.setImageResource(mImageIds[position]);
        container.addView(imageView, 0);
        return imageView;
    }
    @Override
    public void destroyItem(ViewGroup container, int position, Object object) {
        container.removeView((ImageView) object);
    }
}